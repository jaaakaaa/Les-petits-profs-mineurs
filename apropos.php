<h1> A Propos </h1>
<h2 align="center"> Les petits professeurs : Qu’est-ce que c’est ? </h2>

<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Dans le cadre des projets scientifiques de Mines Nancy, des élèves en première année du cursus d'Ingénieur Civil des Mines s'intéressent à la création d'un site internet. Ce dernier a pour vocation de promouvoir l'accès aux sciences et d'attiser la curiosité de jeunes élèves de l'école primaire, de niveau CM1/CM2, voir celle des collégiens de 6e/5e. </p>
<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ce site est composé de différentes sous-sections abordant différents sujets scientifiques : les mathématiques, la matière, l'optique, l'énergie, l'espace... Certains articles sont plus accessibles que d'autres, et la plupart d'entre eux proposent un petit quizz final pour vérifier si l'élève a bien compris. Nous proposons aussi une section avec différentes expériences plus ou moins simples, certaines étant même faciles à reproduire chez soi ou en classe. </p>
<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Le projet a été lancé en 2017 par un groupe d'étudiant de première année, et leur flambeau a été repris par des étudiants de la génération suivante. </p>

