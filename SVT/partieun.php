  <h2 style="color:rgb(5, 163, 35)" > Qu'est ce que c'est la biodiversité </h2>
<br><div id="texte">
    <p> 
    La biodiversité repose sur 3 composantes principales :
</p><li>La variation génétique : C’est la variation de chaque individu au sein d’une population, la variation entre les populations.</li>
<li>La diversité de chaque espèce dans son écosystème ou dans toute la biosphère.</li>
<li>La variété des écosystèmes au sein de la biosphère.</li>
 <p></p>
    <br>
    <p>
    La biodiversité est primordiale, elle assure l’équilibre et protège chaque espèce. Toute perte de diversité peut avoir des conséquences dangereuses :
</p><li>Si une population locale d’une espèce disparait, cette dernière perd une partie de la diversité génétique responsable des adaptations.</li>
<li>L’extinction d’une espère perturbe l’équilibre des chaînes trophiques.</li>
<li>La modification d’un écosystème peut altérer l’ensemble de la biosphère.</li>
<p></p>
    <br>
      <p> </div>
</p><li>Par exemple :</li>
<li>Le phytoplancton des océans consomme des quantités massives du CO2, un des gaz responsables de l’effet de serre, sa diminution entraîne des résultats catastrophiques.</li>
